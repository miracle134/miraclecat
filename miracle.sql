CREATE DATABASE `MIRACLE_CAT`;

DROP TABLE `MEMBER`;
CREATE TABLE `MEMBER` (
    `MEMBER_ID` bigint(20) NOT NULL AUTO_INCREMENT COMMENT '회원 아이디',
    `EMAIL` varchar(100) NOT NULL COMMENT '사용자 아이디',
    `PASSWORD` varchar(64) NOT NULL COMMENT '비밀번호',
    `NAME` varchar(20) NOT NULL COMMENT '사용자 이름',
    `ZIP_CODE` varchar(10) DEFAULT NULL COMMENT '우편번호',
    `ADDRESS` varchar(255) DEFAULT NULL COMMENT '사용자 주소',
    `CELL_PHONE` varchar(20) DEFAULT NULL COMMENT '연락처',
    `CREATED_AT` datetime NOT NULL COMMENT '가입 일시',
    `LAST_UPDATED_ID` bigint(20) DEFAULT NULL COMMENT '최종 수정자 아이디',
    `LAST_UPDATED_AT` datetime DEFAULT NULL COMMENT '최종 수정 일시',
    PRIMARY KEY (`MEMBER_ID`),
    UNIQUE KEY `UK_MEMBER_EMAIL` (`EMAIL`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='회원';

