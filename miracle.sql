DROP TABLE `NOTICE`;
CREATE TABLE `NOTICE` (
    `NOTICE_ID` bigint(20) NOT NULL AUTO_INCREMENT COMMENT '공지사항아이디',
    `TITLE` varchar(64) NOT NULL COMMENT '제목',
    `CONTENT` text NOT NULL COMMENT '내용',
    `OPEN` char(1) CHARACTER SET latin1 NOT NULL DEFAULT 'Y' COMMENT '공개여부',
    `CREATED_ID` bigint(20) NOT NULL COMMENT '등록자',
    `CREATED_AT` datetime NOT NULL COMMENT '등록일자',
    `LAST_UPDATED_ID` bigint(20) DEFAULT NULL COMMENT '수정자',
    `LAST_UPDATED_AT` datetime DEFAULT NULL COMMENT '수정일자',
    PRIMARY KEY (`NOTICE_ID`),
    KEY `FK_NOTICE_CREATED` (`CREATED_ID`),
    KEY `FK_NOTICE_LAST_UPDATED` (`LAST_UPDATED_ID`),
    CONSTRAINT `FK_NOTICE_CREATED` FOREIGN KEY (`CREATED_ID`) REFERENCES `MEMBER` (`MEMBER_ID`),
    CONSTRAINT `FK_NOTICE_LAST_UPDATED` FOREIGN KEY (`LAST_UPDATED_ID`) REFERENCES `MEMBER` (`MEMBER_ID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='공지사항관리';

